#Las variables pueden ser de diferentes tipos, como enteros, flotantes, cadenas, etc.
#Variables can be of different types, such as integers, floats, strings, etc.

#Declaracion de variables 
#Variable declaration
numero1 = 10        # Este es un número entero (integer)
numero2 = 20        # Este es otro número entero (another integer)
numero3 = 100       # Y este es otro número entero más (and yet another integer)

#Imprimir valor de las variables
#Print the value of the variables
print(numero1, numero2, numero3)  # Esto imprimirá "10 20 100" (This will print "10 20 100")

#Variables de diferentes tipos
#Variables of different types
cadena = "Hola, mundo!"  # Esto es una cadena de texto (string)
flotante = 3.14          # Esto es un número flotante (float)

#Imprimir valor de las variables adicionales
#Print the value of the additional variables
print(cadena, flotante)  # Esto imprimirá "Hola, mundo! 3.14" (This will print "Hola, mundo! 3.14")
